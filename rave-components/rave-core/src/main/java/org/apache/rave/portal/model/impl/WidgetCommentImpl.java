package org.apache.rave.portal.model.impl;


import org.apache.rave.model.WidgetComment;

import java.util.Date;

public class WidgetCommentImpl implements WidgetComment {
    private String id;
    private String userId;
    private String text;
    private Date lastModifiedDate;
    private Date createdDate;

    public WidgetCommentImpl() {}

    public WidgetCommentImpl(String id) {
        this.id = id;
    }

    @Override
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String getUserId() {
        return userId;
    }

    @Override
    public void setUserId(String userId) {
        this.userId = userId;
    }

    @Override
    public String getText() {
        return text;
    }

    @Override
    public void setText(String text) {
        this.text = text;
    }

    @Override
    public Date getLastModifiedDate() {
        return lastModifiedDate;
    }

    @Override
    public void setLastModifiedDate(Date lastModifiedDate) {
        this.lastModifiedDate = lastModifiedDate;
    }

    @Override
    public Date getCreatedDate() {
        return createdDate;
    }

    @Override
    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) return true;
        if (obj == null || getClass() != obj.getClass()) return false;
        WidgetCommentImpl comment = (WidgetCommentImpl) obj;
        if (id != null ? !id.equals(comment.id) : comment.id != null) return false;
        return true;
    }
}
